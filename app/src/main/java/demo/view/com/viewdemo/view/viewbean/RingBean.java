package demo.view.com.viewdemo.view.viewbean;

public class RingBean {
    private int color;
    private int baifenbi;
    private String centetText;
    private float startAngle;
    private float endAngle;

    public float getStartAngle() {
        return startAngle;
    }

    public void setStartAngle(float startAngle) {
        this.startAngle = startAngle;
    }

    public float getEndAngle() {
        return endAngle;
    }

    public void setEndAngle(float endAngle) {
        this.endAngle = endAngle;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getBaifenbi() {
        return baifenbi;
    }

    public void setBaifenbi(int baifenbi) {
        this.baifenbi = baifenbi;
    }

    public String getCentetText() {
        return centetText;
    }

    public void setCentetText(String centetText) {
        this.centetText = centetText;
    }
}
